''' file contains stop words class for finding and removing stop words from a definition '''


class StopWords(object):
    def __init__(self):
        self.populate()

    def __contains__(self, word):
        return word in self.stopWordHash

    def isStopWord(self, word):
        return word in self.stopWordHash

    def removeStopWords(self, defn):
        ''' given a def of words separated by spaces, remove the stop words and return the new def '''
        words = defn.split(" ")
        newWords = []
        for word in words:
            if word in self.stopWordHash:
                continue
            else:
                # take the word if it was not in the stopwords hash
                newWords.append(word)
        newDef = " ".join(newWords)
        return newDef

    def getHash(self):
        return self.stopWordHash

